# 2.2 Текстовые надписи на графике.
# Наиболее часто используемые текстовые элементы на графике:
# • наименования осей (xlabel, ylabel)
# • заголовок графика (title)
# • текстовые примечания непосредственно на поле графика (text)
# • легенда (legend)


# 2.2.1 Наименование осей
# Для подписи оси X используется plt.xlabel()
# Для подписи оси Y используется plt.ylabel()
#
# Основные аргументы:
#   xlabel / ylabel: str          → сам текст подписи
#   labelpad: float или None      → расстояние от метки до области графика (по умолчанию None)
#
# Дополнительно можно использовать параметры класса matplotlib.text.Text:
#   fontsize / size     → размер шрифта (число или строка: 'small', 'medium', 'large', 'x-large' и др.)
#   fontstyle           → 'normal', 'italic', 'oblique'
#   fontweight          → число 0–1000 или строки: 'normal', 'bold', 'semibold', 'light' и др.
#   color               → цвет текста ('red', '#FF0000', 'blue' и т.д.)


# 2.2.2 Заголовок графика
# plt.title() — задаёт заголовок всего графика
#
# Основные аргументы:
#   label: str          → текст заголовка
#   loc: str            → выравнивание: 'center' (по умолчанию), 'left', 'right'
#
# Также поддерживаются параметры текста:
#   fontsize, fontstyle, fontweight, color и др. (аналогично xlabel/ylabel)


# 2.2.3 Текстовое примечание (произвольный текст на поле графика)
# plt.text(x, y, s) — размещает текст в указанных координатах
#
# Аргументы:
#   x, y          → координаты левого нижнего угла текста (в единицах данных графика)
#   s             → строка текста
#
# Дополнительно можно использовать все параметры matplotlib.text.Text


# 2.2.4 Легенда
# plt.legend() — отображает легенду
# Легенда появится только если у графиков задан параметр label='...'
# В этом уроке подробные настройки легенды не рассматриваются

# Пример кода со всеми перечисленными элементами:

import matplotlib.pyplot as plt

# Данные для примера
x = [1, 5, 10, 15, 20]
y = [1, 7, 3, 5, 11]

# Построение графика с подписью для легенды
plt.plot(x, y, label='steel price')           # label → будет использовано в легенде

# Заголовок графика
plt.title('Chart price', fontsize=15)

# Подписи осей с настройкой шрифта и цвета
plt.xlabel('Day',   fontsize=12, color='blue')
plt.ylabel('Price', fontsize=12, color='blue')

# Включаем легенду (показывает все элементы с label)
plt.legend()

# Включаем сетку (по умолчанию основная сетка)
plt.grid(True)

# Произвольная текстовая надпись на поле графика
plt.text(15, 4, 'grow up!', fontsize=11, color='darkgreen')

# Показываем график
plt.show()